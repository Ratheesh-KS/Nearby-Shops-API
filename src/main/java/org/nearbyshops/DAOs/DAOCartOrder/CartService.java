	public ArrayList<Cart> readCarts(Integer endUserID, Integer shopID)
	{
		String query = "SELECT * FROM " + Cart.TABLE_NAME;

		boolean isFirst = true;

		if(endUserID != null)
		{
			query = query + " WHERE " + Cart.END_USER_ID + " = " + endUserID;

			isFirst = false;
		}

		if(shopID != null)
		{
			if(isFirst)
			{
				query = query + " WHERE " + Cart.SHOP_ID + " = " + shopID;

			}else
			{
				query = query + " AND " + Cart.SHOP_ID + " = " + shopID;

			}

		}



		ArrayList<Cart> cartsList = new ArrayList<Cart>();
		
		Connection connection = null;
		Statement statement = null;
		ResultSet rs = null;
		
		try {
			
			connection = dataSource.getConnection();
			statement = connection.createStatement();
			
			rs = statement.executeQuery(query);
			
			while(rs.next())
			{

				Cart cart = new Cart();

				cart.setCartID(rs.getInt(Cart.CART_ID));
				cart.setEndUserID(rs.getInt(Cart.END_USER_ID));
				cart.setShopID(rs.getInt(Cart.SHOP_ID));

				cartsList.add(cart);
				
			}
			

			
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		

		finally
		
		{
			
			try {
					if(rs!=null)
					{rs.close();}
				} catch (SQLException e) {
					// TODO Auto-generated catch block
					e.printStackTrace();
				}
			
			try {
			
				if(statement!=null)
				{statement.close();}
			} catch (SQLException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
			
			try {
				
				if(connection!=null)
				{connection.close();}
			} catch (SQLException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
		}
		
								
		return cartsList;
	}



	
